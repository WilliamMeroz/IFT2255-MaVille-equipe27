package maville.equipe27.enums;

/**
 * Enum représentant les différentes options de choix d'authentification.
 * Ces choix permettent à un utilisateur de sélectionner une action lors de l'authentification.
 */
public enum AuthChoices {
    INSCRIPTION, // Option pour l'inscription d'un nouvel utilisateur
    CONNECTION,  // Option pour la connexion d'un utilisateur existant
    QUITTER      // Option pour quitter l'application
}
